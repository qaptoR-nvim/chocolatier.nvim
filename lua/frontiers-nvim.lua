---@param config FrontiersConfig?
Frontiers.setup = function(config)
  Frontiers.config = vim.tbl_deep_extend("force", Frontiers.config, config or {})
end

--- main load function
Frontiers.load = function()
  if vim.version().minor < 8 then
    vim.notify_once("frontiers.nvim: you must use neovim 0.8 or higher")
    return
  end

  -- reset colors
  if vim.g.colors_name then
    vim.cmd.hi("clear")
  end
  vim.g.colors_name = "frontiers-nvim"
  vim.o.termguicolors = true

  local groups = get_groups()

  -- add highlights
  for group, settings in pairs(groups) do
    vim.api.nvim_set_hl(0, group, settings)
  end
end

return Frontiers
